// 56. Take 3 string inputs and print them in reverse order. 

#include <iostream>
using namespace std;
int main() {
    string str1, str2, str3;
    cout << "Enter three strings: ";
    cin >> str1 >> str2 >> str3;

    cout << "Strings in reverse order:" << endl;
    cout << str3 << endl;
    cout << str2 << endl;
    cout << str1 << endl;

    return 0;
}
