// 8. Print the sum of two user-entered numbers.  

#include <bits/stdc++.h>
using namespace std;
int main(){
    
    int a, b;
    cout << "Enter first number: ";
    cin >> a;
    cout << "Enter second number: ";
    cin >> b;

    int c = a + b;
    cout << "The sum is: " << c << endl;

    return 0;
}